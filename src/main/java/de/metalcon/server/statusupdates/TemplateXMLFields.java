package de.metalcon.server.statusupdates;

/**
 * field identifiers used within XML status update template files
 * 
 * @author sebschlicht
 * 
 */
public class TemplateXMLFields {

	/**
	 * value of attributes that were not set
	 */
	public static final String NO_VALUE = "";

	/**
	 * template root element
	 */
	public static final String TEMPLATE_ROOT = "class";

	/**
	 * template name
	 */
	public static final String TEMPLATE_NAME = "name";

	/**
	 * template version
	 */
	public static final String TEMPLATE_VERSION = "version";

	/**
	 * template item name
	 */
	public static final String ITEM_NAME = "name";

	/**
	 * template field information
	 */
	public static final String FIELD = "param";

	/**
	 * template field type
	 */
	public static final String FIELD_TYPE = "type";

	/**
	 * template file information
	 */
	public static final String FILE = "file";

	/**
	 * template file content type
	 */
	public static final String FILE_CONTENT_TYPE = "contentType";

}